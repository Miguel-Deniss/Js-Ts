/*
Let é uma variavel
Variavel pode ser alterada quando quiser
Variaveis precisam ter significado
Variaveis não podem ser com palavras reservadas
Não pode começar o nome de uma variavel com numero "let 1nome", vai dar erro
Não pode ter espaço ou traços
Utilizamos camelCase, todas as palavras depois da primeira começam com letra maiuscula para separar
Não podemos redeclarar a variavel com LET
*/



let nome;
nome = 'Miguel';
console.log(nome);
nome = 'João';
console.log(nome);

let nomeCompleto = "Miguel Denisovas";
console.log(nomeCompleto)

